
public class typecast {

	public static void main(String[] args) {
		
		byte age=24;     // Implicit Type Casting
		int new_age=age;
		System.out.println("Age is "+new_age);
		
		int sal=30000;     // Explicit Type casting
		short new_sal=(short)sal;
		System.out.println("Salary is "+new_sal);
		
	}

}
